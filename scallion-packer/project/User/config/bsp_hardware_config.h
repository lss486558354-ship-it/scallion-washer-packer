#ifndef __BSP_HARDWARE_CONFIG_H__
#define __BSP_HARDWARE_CONFIG_H__

/* =============================================================================
 * TIM4 与步进 / PWM 舵机（与《外设接线表_PWM步进六定时器优化版》一致）
 * =============================================================================
 * 1：TIM4 全重映射到 PD12(CH1)/PD13(CH2) 等，供步进5/6 PUL；PB6~PB9 不再作为 TIM4。
 *    此时 bsp_servo / bsp_servo_obj 中基于 TIM4@PB6~9 的 PWM 舵机初始化会跳过，请用 UART4 总线舵机。
 * 0：恢复旧行为（TIM4 默认脚 PB6~9，与步进 V6 接线不兼容，勿与 PD12/13 丝杆同时用）。
 */
#define BSP_TIM4_STEPPER_REMAP_V6    1

/* =============================================================================
 * PWM 示波器/接线测试模式（全机应用 main 走 device.c 时有效）
 * =============================================================================
 * 1：上电后步进 TIM1/2/3/4/5 各 PUL 引脚输出约 50% 占空比连续方波（约 1kHz），
 *    并关闭步进用更新中断，避免位置计数刷屏。
 *    DCM_InitAll 之后对 TIM8 四路（PC6~PC9）同样输出约 50% 占空比连续 PWM（10kHz 时基）。
 * 0：正常业务；步进/直流由 Run/Stop 控制。
 * 注意：TIM8 演示工程 User/main.c 另见该文件内对 ATIM 的连续 50% 输出开关。
 * 舵机：V6 下 TIM4@PB6~9 舵机已跳过；UART 舵机不在此宏控制范围内。
 */
#define BSP_PWM_FORCE_50_CONTINUOUS    1

/* =============================================================================
 * 驱动器接法配置（V8.0统一标准）
 * ============================================================================= */

/*
 * V8.0统一标准：所有驱动器 ENA 引脚均不接，驱动器默认使能
 * PUL+/DIR+/ENA+ 全部接 STM32
 * PUL-/DIR-/ENA- 全部接 GND
 * 
 * 注意：本版本不再使用 ENA 功能，所有步进电机始终使能
 */

/* =============================================================================
 * 驱动器ENA使能引脚配置
 * ============================================================================= */

/*
 * V8.0标准：所有驱动器 ENA 引脚均不接，驱动器默认使能
 * 但代码中仍保留 ENA 相关宏，以便将来扩展
 */
#define BSP_ENA_ENABLE(pin)    /* 空操作：驱动器默认使能 */
#define BSP_ENA_DISABLE(pin)   /* 空操作：驱动器默认使能 */

/* =============================================================================
 * 方向引脚(DIR)电平配置
 * ============================================================================= */

/*
 * 选择 DIR 引脚电平逻辑：
 *   DIR_LEVEL_NORMAL   - 高电平=正转，低电平=反转（默认）
 *   DIR_LEVEL_INVERTED - 低电平=正转，高电平=反转
 */
#define DIR_LEVEL_NORMAL

#if defined(DIR_LEVEL_NORMAL)
    /* 高电平正转，低电平反转 */
    #define BSP_DIR_FORWARD(pin)    GPIO_SetHigh(pin)
    #define BSP_DIR_BACKWARD(pin)   GPIO_SetLow(pin)
#elif defined(DIR_LEVEL_INVERTED)
    /* 低电平正转，高电平反转 */
    #define BSP_DIR_FORWARD(pin)    GPIO_SetLow(pin)
    #define BSP_DIR_BACKWARD(pin)   GPIO_SetHigh(pin)
#endif

/* =============================================================================
 * 限位开关类型配置
 * ============================================================================= */

/*
 * 选择限位开关类型：
 *   LIMIT_SWITCH_TYPE_NO - 常开型（未触发时断开，触发时闭合）
 *   LIMIT_SWITCH_TYPE_NC - 常闭型（未触发时闭合，触发时断开）
 */
#define LIMIT_SWITCH_TYPE_NO

/* =============================================================================
 * 输出配置验证（编译时检查）
 * ============================================================================= */

#if !defined(DIR_LEVEL_NORMAL) && !defined(DIR_LEVEL_INVERTED)
    #error "bsp_hardware_config.h: 请选择方向引脚电平逻辑！\
           在文件顶部添加 #define DIR_LEVEL_NORMAL 或 \
           #define DIR_LEVEL_INVERTED"
#endif

#endif /* __BSP_HARDWARE_CONFIG_H__ */


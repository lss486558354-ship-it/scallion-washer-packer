#ifndef __BSP_TICK_H__
#define __BSP_TICK_H__

#include "stm32f10x.h"

/* 初始化SysTick
 * 配置为1ms中断一次
 */
void BSP_Tick_Init(void);

/* SysTick中断回调（供 stm32f10x_it.c 的 SysTick_Handler 调用） */
void BSP_Tick_OnSysTick(void);

/* 应用层 1ms 节拍（在 BSP_Tick_OnSysTick 内调用；在 main.c 实现步进流程定时等） */
void BSP_Tick_AppHook1ms(void);

/* 获取当前毫秒数
 * 返回：uint32_t类型，计数值
 * 说明：从0开始，每1ms加1，约49.7天溢出
 */
uint32_t BSP_GetTickMs(void);

#endif

